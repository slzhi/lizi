
//演示变量的使用
public class Var02{
	
	public static void main(String[] args){
		
		//记录人的信息
		int age = 16; 			//年龄
		
		double score = 88.9;	//得分
		
		char gender = '男';		//性别
		
		String name = "King";	//姓名
		
		//输出信息，快捷键
		System.out.println("人的信息如下\n姓名\t性别\t年龄\t得分");
		
		System.out.println(name + "\t" + gender + "\t" + age + "\t" + score);
		
	}
	
}