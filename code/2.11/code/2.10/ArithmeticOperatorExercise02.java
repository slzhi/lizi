
//算术运算符练习
public class ArithmeticOperatorExercise02{
	
	public static void main(String[] args){
		//自增，自减课堂练习，看看输出什么
		int i1 = 10;
		int i2 = 20;
		int i = i1++;
		System.out.println("i=" + i);//10
		System.out.println("i=" + i1);//11
		
		int a = --i2;
		System.out.println("a=" + a);//19
		System.out.println("i2=" + i2);//19
		
	}
	
}